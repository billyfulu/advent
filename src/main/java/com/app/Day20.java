    private int getCountHsh(List<String> outputString) {
        int countHsh = 0;
        for (String s : outputString) {
            for (char c : s.toCharArray()) {
                if ('#' == c) {
                    countHsh++;
                }
            }
        }
        return countHsh;
    }

